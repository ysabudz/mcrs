function displayPopup(title, message, onClose) {
    // Create the overlay
    const overlay = document.createElement('div');
    overlay.classList.add('popup-overlay');

    // Create the notification box
    const notification = document.createElement('div');
    notification.classList.add('popup-notification');

    // Create the title
    const titleElement = document.createElement('h2');
    titleElement.textContent = title;

    // Create the message
    const messageElement = document.createElement('p');
    messageElement.textContent = message;

    // Create the close button
    const closeButton = document.createElement('button');
    closeButton.classList.add('popup-button');
    closeButton.textContent = 'OK';
    closeButton.addEventListener('click', () => {
        overlay.remove(); // Remove the overlay and notification
        if (onClose) {
            onClose(); // Call the onClose callback if provided
        }
    });

    // Append elements to the notification box
    notification.appendChild(titleElement);
    notification.appendChild(messageElement);
    notification.appendChild(closeButton);

