<script>
    document.addEventListener('DOMContentLoaded', function() {
        const tableRows = document.querySelectorAll('table tbody tr');
        const searchInput = document.getElementById('nameSearch');
        const studentsCheckbox = document.getElementById('studentsCheckbox');
        const blueLabelElement = document.getElementById('blueLabel');
        const purpleLabelElement = document.getElementById('purpleLabel');
        const orangeLabelElement = document.getElementById('orangeLabel');
        const pinkLabelElement = document.getElementById('pinkLabel');
        const greenLabelElement = document.getElementById('greenLabel');
        const yellowLabelElement = document.getElementById('yellowLabel');
        const blueSquare = blueLabelElement.querySelector('.square');
        const purpleSquare = purpleLabelElement.querySelector('.square');
        const orangeSquare = orangeLabelElement.querySelector('.square');
        const pinkSquare = pinkLabelElement.querySelector('.square');
        const greenSquare = greenLabelElement.querySelector('.square');
        const yellowSquare = yellowLabelElement.querySelector('.square');
        const studentsSquare = document.querySelector('#studentsLabel .square');

        const checkboxes = [
            { checkbox: studentsCheckbox, square: studentsSquare, defaultColor: 'black' },
            { checkbox: document.getElementById('blueCheckbox'), square: blueSquare, defaultColor: 'blue' },
            { checkbox: document.getElementById('purpleCheckbox'), square: purpleSquare, defaultColor: 'purple' },
            { checkbox: document.getElementById('orangeCheckbox'), square: orangeSquare, defaultColor: 'orange' },
            { checkbox: document.getElementById('pinkCheckbox'), square: pinkSquare, defaultColor: 'pink' },
            { checkbox: document.getElementById('greenCheckbox'), square: greenSquare, defaultColor: 'green' },
            { checkbox: document.getElementById('yellowCheckbox'), square: yellowSquare, defaultColor: 'yellow' }
        ];

        tableRows.forEach(row => {
            row.style.cursor = 'pointer';
            row.addEventListener('click', function() {
                const href = this.getAttribute('data-href');
                if (href) {
                    window.location.href = href;
                }
            });
        });

        searchInput.addEventListener('input', function() {
            const searchTerm = this.value.toLowerCase();
            tableRows.forEach(row => {
                const studentName = row.querySelector('td:first-child').textContent.toLowerCase();
                if (studentName.includes(searchTerm)) {
                    row.style.display = '';
                } else {
                    row.style.display = 'none';
                }
            });
        });

        // Initialize all squares to gray.
        checkboxes.forEach(item => {
            item.square.style.backgroundColor = '#ccc';
            item.checkbox.checked = false; // Ensure checkboxes are initially unchecked
        });

        studentsCheckbox.addEventListener('change', function() {
            checkboxes.forEach(item => {
                item.square.style.backgroundColor = this.checked ? item.defaultColor : '#ccc';
            });
        });

        // Add individual click handlers for the color squares.
        checkboxes.slice(1).forEach(item => { // Skip the first one (Students)
            item.checkbox.addEventListener('change', function() {
                item.square.style.backgroundColor = this.checked ? item.defaultColor : '#ccc';
            });
        });
    });
</script>

